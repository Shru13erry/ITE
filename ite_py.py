# Q-Learning
ALPHA            = 0.1      # learning rate         (0 = never learn, 1 = forget past)
GAMMA            = 0.9      # discount factor       (how much future rewards matter)
EPSILON_START    = 1.0      # initial exploration   (100 % random at first)
EPSILON_MIN      = 0.05     # minimum exploration   (always keep 5 % curiosity)
EPSILON_DECAY    = 0.995    # per-episode decay     (slowly shift to exploitation)

# Network / topology
NUM_SWITCHES     = 5        # s1 … s5
MAX_PORTS        = 8        # maximum ports per switch in our topology
