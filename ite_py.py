# ─────────────────────────────────────────────────────────────────────────────
#  Q-Learning Agent
# ─────────────────────────────────────────────────────────────────────────────
class QLearningAgent:
    """
    Tabular Q-Learning agent.

    State  : (source_dpid, destination_mac)   → encoded as an integer index
    Action : next-hop output port on the current switch  (1 … MAX_PORTS)

    Q-Table shape : [num_states, MAX_PORTS]
    """

    def __init__(self, num_states: int, num_actions: int):
        self.q_table  = np.zeros((num_states, num_actions), dtype=np.float32)
        self.epsilon  = EPSILON_START
        self.episode  = 0
        LOG.info('Q-Table initialised  shape=%s', self.q_table.shape)

    # ── Core update (Bellman equation) ────────────────────────────────────
    def update(self, state: int, action: int, reward: float, next_state: int):
        """
        Q(s,a) ← Q(s,a) + α · [r + γ · max Q(s',·) − Q(s,a)]
        """
        if state >= self.q_table.shape[0] or next_state >= self.q_table.shape[0]:
            return                           # guard against index overflow

        current_q  = self.q_table[state, action]
        future_max = np.max(self.q_table[next_state])
        td_target  = reward + GAMMA * future_max
        td_error   = td_target - current_q
        self.q_table[state, action] += ALPHA * td_error

    # ── Action selection (ε-greedy) ───────────────────────────────────────
    def choose_action(self, state: int, valid_ports: list) -> int:
        """
        With probability ε pick a random port (explore).
        Otherwise pick the port with the highest Q-value (exploit).
        Returns a valid port number (1-indexed).
        """
        if not valid_ports:
            return 1                         # fallback

        if random.random() < self.epsilon:
            return random.choice(valid_ports)

        if state >= self.q_table.shape[0]:
            return random.choice(valid_ports)

        # Map valid ports to action indices (0-indexed)
        port_q = {p: self.q_table[state, p - 1] for p in valid_ports}
        best   = max(port_q, key=port_q.get)
        return best

    # ── Epsilon decay (called after each monitoring round) ────────────────
    def decay_epsilon(self):
        self.episode += 1
        self.epsilon  = max(EPSILON_MIN, self.epsilon * EPSILON_DECAY)

    # ── Serialise Q-Table for inspection ─────────────────────────────────
    def save_q_table(self, path: str = 'q_table.npy'):
        np.save(path, self.q_table)
        LOG.info('Q-Table saved → %s', path)


# ─────────────────────────────────────────────────────────────────────────────
#  Reward Calculator
# ─────────────────────────────────────────────────────────────────────────────
class RewardCalculator:
    """
    Converts raw port statistics into a scalar reward signal.

    reward = W_BW  * norm(avail_bw)
           + W_DELAY * norm(latency)
           + W_LOSS  * loss_pct
    """

    def __init__(self):
        self._prev_stats: dict = {}          # (dpid, port) → (bytes, time)

    def compute(self,
                dpid       : int,
                port       : int,
                tx_bytes   : int,
                rx_bytes   : int,
                duration_s : float) -> dict:
        """
        Returns a dict with avail_bw_mbps, latency_ms, loss_pct, reward.
        """
        key       = (dpid, port)
        now       = time.time()

        # ── Bandwidth ─────────────────────────────────────────────────────
        if key in self._prev_stats:
            prev_tx, prev_rx, prev_time = self._prev_stats[key]
            dt        = max(now - prev_time, 0.001)
            tx_rate   = (tx_bytes - prev_tx) * 8 / dt          # bps
            rx_rate   = (rx_bytes - prev_rx) * 8 / dt          # bps
            used_bps  = max(tx_rate, rx_rate)
        else:
            used_bps  = 0.0

        self._prev_stats[key] = (tx_bytes, rx_bytes, now)

        cap_bps       = LINK_CAPACITY_BPS['hub'] if port >= 5 else LINK_CAPACITY_BPS['ring']
        avail_bps     = max(cap_bps - used_bps, 0.0)
        avail_bw_mbps = avail_bps / 1_000_000
        norm_bw       = avail_bps / cap_bps             # 0 … 1

        # ── Simulated latency (μs → ms)  ──────────────────────────────────
        # In a real deployment this would come from LLDP/OAM probes.
        # Here we simulate it proportionally to link utilisation.
        utilisation   = min(used_bps / cap_bps, 1.0)
        base_delay_ms = 1.0 if port >= 5 else 5.0
        latency_ms    = base_delay_ms * (1.0 + 4.0 * utilisation)  # 1–5× under load

        # ── Simulated packet loss ─────────────────────────────────────────
        # Loss spikes sharply above 90 % utilisation (tail-drop model)
        loss_pct      = max(0.0, (utilisation - 0.9) * 100.0) if utilisation > 0.9 else 0.0

        # ── Reward ────────────────────────────────────────────────────────
        reward = (W_BW    * norm_bw
                  + W_DELAY * (latency_ms / 20.0)   # normalise to ~[0,1]
                  + W_LOSS  * (loss_pct  / 100.0))

        return {
            'avail_bw_mbps': round(avail_bw_mbps, 3),
            'latency_ms'   : round(latency_ms,    3),
            'loss_pct'     : round(loss_pct,       3),
            'reward'       : round(reward,         4),
        }


# ─────────────────────────────────────────────────────────────────────────────
